s1 = {1, 2, 3, 4, 5}
s2 = {4, 5, 6, 7, 8}
print(s1.union(s2)) #returns a new set that is the union of s1 and s2
print(s1 | s2) #returns a new set that is the union of s1 and s2
print(s1.intersection(s2)) #returns a new set that is the intersection of s1 and s2
print(s1 & s2) #returns a new set that is the intersection of s1 and s2
print(s1.difference(s2)) #returns a new set that is the difference of s1 and s2
print(s1 - s2) #returns a new set that is the difference of s1 and s2
print(s1.symmetric_difference(s2)) #returns a new set that is the symmetric difference of s1 and s2
print(s1 ^ s2) #returns a new set that is the symmetric difference of s1 and s2     
